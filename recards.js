function gtColor1(clickedDiv) {
    event.preventDefault();
    var targetDiv = document.getElementById('colorChange_gt');

    // Toggle background color of the target div
    if (targetDiv.style.backgroundImage === 'url("https://www.royalenfield.com/content/dam/royal-enfield/india/motorcycles/continental-gt/new/colours/studio-shots/slipstream-blue/new/slipstream_blue_0000.png")') {
        targetDiv.style.backgroundImage = 'url("https://www.royalenfield.com/content/dam/royal-enfield/india/motorcycles/continental-gt/new/colours/studio-shots/slipstream-blue/new/slipstream_blue_0000.png")';
    } else {
        targetDiv.style.backgroundImage = 'url("https://www.royalenfield.com/content/dam/royal-enfield/india/motorcycles/continental-gt/new/colours/studio-shots/slipstream-blue/new/slipstream_blue_0000.png")';
    }

    var targetDiv = document.getElementById('changeBorder_gt');

    if (targetDiv.style.border === '4px solid #256fef;' && targetDiv.style.outline === '2px solid #90c4ef;') {
        targetDiv.style.border = '4px solid #256fef;';
        targetDiv.style.outline = '2px solid #90c4ef;';
    } else {
        targetDiv.style.border = '4px solid #256fef;';
        targetDiv.style.outline = '2px solid #90c4ef;';
    }

}

